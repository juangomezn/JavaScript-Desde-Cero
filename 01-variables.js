//var -> Manera inicial de declarar variables No recomendada de usar

var helloWorld = "!Hola, JavaScript!"
console.log(helloWorld)

helloWorld = "!Hola de nuevo, JavaScript!"
console.log(helloWorld)

/*let -> Se pueden llamar a ejecucion a traves de un console log pero 
despues de ser inicializada ya que de no ser asi daran error*/

let helloWorld2 = "!Hola, JavaScript 2!"
console.log(helloWorld2)

helloWorld2 = "!Hola de nuevo, JavaScript 2!"
console.log(helloWorld2)

/*const -> Se declaran variables constantes, es decir, no se pueden cambiar 
su valor luego de ser inicializadas*/

const helloWorld3 = "!Hola, JavaScript 3!"
console.log(helloWorld3)

